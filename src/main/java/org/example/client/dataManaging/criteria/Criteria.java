package org.example.client.dataManaging.criteria;

/**
 * Интерфейс для определения критериев валидации строковых значений.
 * <p>
 * Реализации этого интерфейса используются для проверки соответствия значения определённым условиям
 * и предоставления сообщений об ошибках.
 * </p>
 *
 * @author Aerosolus
 * @version 1.0
 * @since 1.0
 */
public interface Criteria {

    /**
     * Проверяет значение на соответствие критерию.
     *
     * @param value проверяемое значение (может быть {@code null})
     * @return {@code true} - если значение соответствует критерию,
     *         {@code false} - во всех остальных случаях
     */
    boolean check(String value);

    /**
     * Возвращает сообщение об ошибке для случаев, когда проверка не пройдена.
     *
     * @return Описание ошибки в строковом формате
     */
    String errorMessage();
}
